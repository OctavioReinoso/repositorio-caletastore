class Producto{
    id;
    img;
    titulo;
    TipoPrenda;
    precio;
    cantidad;

    constructor(id, img,titulo, tipoPrenda, precio, cantidad){
        this.id = id;
        this.img = img;
        this.titulo = titulo;
        this.tipoPrenda = tipoPrenda;
        this.precio = precio;
        this.cantidad = cantidad;
    }
}